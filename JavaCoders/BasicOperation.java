// basic
// data type [int,float,double]
// flow
class BasicOperation{
	public static void main(String[] args) {
		
			int a,b,c;
			a = 7;
			b = 2;

			c = a+b;
			System.out.println(a+"+"+b+"="+c); // 19+23 = 42

			c = a-b;
			System.out.println(a+"-"+b+"="+c);

			c = a*b;
			System.out.println(a+"*"+b+"="+c);

			c = a/b;
			System.out.println(a+"/"+b+"="+c);
 
			c = a%b;
			System.out.println(a+"%"+b+"="+c);
			
	}
}