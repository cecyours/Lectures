
#include<stdio.h>
void main()
{
	int x=10;
	
	
	printf("During post increment : %d\n",x++); //x++ // 10
	printf(" after post increment : %d\n",x); // 11
	
	printf("\n-------------------------\n\n");
	
	printf("during pre increment : %d\n",++x); //12
	printf(" after pre increment : %d\n",x); // 12
	
}
