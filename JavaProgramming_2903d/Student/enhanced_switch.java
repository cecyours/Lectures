public class enhanced_switch {
    public static void main(String[] args) {
        int x = 2;
        switch(x)
        {
            case 1 -> System.out.print("Kriti");
            case 2 -> System.out.print("Jeniffer");
            case 3 -> System.out.print("Elizabeth Olsen");
            default -> System.out.print("Zendeya");
        }
    }
    
}
