public class switch_case {
    public static void main(String[] args) {
        char x = 'v';
        switch(x)
        {
          case 'a':
            for(int i = 1; i<=5; i++)
            {
                System.out.println("Kiara");
            }
            break;
          case 'b':
            for(int i = 1; i<=5; i++)
            {
                System.out.println("Sonia");
            }
            break;
          case 'v':
            for(int i = 1; i<=5; i++)
            {
                System.out.println("Samrin");
            }
            break;
          case 'c':
            for(int i = 1; i<=5; i++)
            {
                System.out.println("Avneet");
            }
            break;
          default:
            System.out.println("Bhai to Bhai Hai");
        }
    }
}
