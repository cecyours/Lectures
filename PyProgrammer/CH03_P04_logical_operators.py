
# and : all condition
#  or : any condition
# not : reverse condition

if True and False:
    print("AND I am in")
else:
    print("AND I am wrong..")

if True or False:
    print("OR I am in")
else:
    print("OR I am wrong..")

if not True:
    print("NOT I am true")
else:
    print("NOT I am False")
