
class CH05_P03_decrementIn{
	public static void main(String[] args) {
		// code to input i
		int i = 19;

		System.out.println("during post decrement : "+(i--));//19
		System.out.println(" after post decrement : "+i);// 18

		System.out.println("during pre decrement : "+(--i));//17
		System.out.println(" after pre decrement : "+i);// 17
			
	}
}